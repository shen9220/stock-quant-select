def generate_stock_data(stock_code: str, stock_name: str, lookback_days: int = 40, force_match: bool = False) -> Optional[pd.DataFrame]:
    """
    生成模拟股票数据（完全不依赖网络）
    
    Args:
        force_match: 是否强制生成完全符合条件的数据
    
    确保数据满足以下条件：
    1. 历史有过放量大阳线或涨停板（涨幅≥6%，成交量放量≥1.5倍）
    2. 当日是缩量阴线（收阴线，成交量小于昨日）
    3. 股价回调到10日线附近（±2%范围）
    4. MA10向上翘头（今日MA10 > 昨日MA10）
    """
    try:
        base_price = random.uniform(10, 30)
        dates = pd.date_range(end=datetime.now(), periods=lookback_days + 10, freq='B')

        data = []
        base_volume = 400000  # 基础成交量

        for i, date in enumerate(dates):
            # 基础上涨趋势（确保MA10向上）
            trend = 1 + (i * 0.0008)
            
            # 计算当前的基础价格
            base_current_price = base_price * trend
            
            # 正常交易日的波动
            if i < len(dates) - 10:
                # 前30天：正常波动
                close = base_current_price * random.uniform(0.99, 1.01)
                volume = int(base_volume * random.uniform(0.8, 1.2))
                open_price = close * random.uniform(0.99, 1.01)
            else:
                # 最后10天：精确控制以满足条件
                days_before_end = len(dates) - 1 - i
                
                if days_before_end > 3:
                    # 7-10天前：制造放量大阳线（涨停或大涨）
                    if random.random() < 0.3 or force_match:
                        prev_close = data[-1]['close'] if data else base_current_price
                        # 涨幅≥6%
                        close = prev_close * random.uniform(1.06, 1.10)
                        # 放量≥1.5倍
                        avg_vol = sum(d['volume'] for d in data[-20:]) / min(20, len(data)) if data else base_volume
                        volume = int(avg_vol * random.uniform(1.6, 2.5))
                        open_price = prev_close * random.uniform(1.01, 1.03)
                    else:
                        close = base_current_price * random.uniform(0.99, 1.01)
                        volume = int(base_volume * random.uniform(0.8, 1.2))
                        open_price = close * random.uniform(0.99, 1.01)
                
                elif days_before_end > 1:
                    # 2-3天前：正常调整，让MA10向上
                    close = base_current_price * random.uniform(1.005, 1.015)  # 缓慢上涨
                    volume = int(base_volume * random.uniform(0.9, 1.1))
                    open_price = close * random.uniform(0.995, 1.005)
                
                else:
                    # 最后1-2天：制造完美的回调阴线
                    # 预先计算MA10，确保价格在MA10附近
                    if i == len(dates) - 1:
                        # 最后一天：必须是完美的缩量阴线回调
                        
                        # 计算前20天的收盘价用于预估MA10
                        recent_closes = [d['close'] for d in data[-10:]]
                        ma10_estimate = sum(recent_closes) / len(recent_closes) if recent_closes else base_price
                        
                        # 价格在MA10的±2%范围内
                        target_close = ma10_estimate * random.uniform(0.98, 1.02)
                        
                        # 必须是阴线：开盘价 > 收盘价
                        open_price = target_close * random.uniform(1.005, 1.015)
                        close = open_price * random.uniform(0.975, 0.990)  # 阴线
                        
                        # 必须缩量：成交量 < 前一日
                        prev_volume = data[-1]['volume'] if data else base_volume
                        volume = int(prev_volume * random.uniform(0.7, 0.9))
                        
                        # 确保MA10向上（当前价格应该略高于之前的价格）
                        close = max(close, ma10_estimate * 1.001)
                    else:
                        # 倒数第二天
                        close = base_current_price * random.uniform(1.01, 1.02)
                        volume = int(base_volume * random.uniform(1.1, 1.3))  # 放一点量
                        open_price = close * random.uniform(0.99, 1.01)

            high = max(open_price, close) * random.uniform(1.00, 1.015)
            low = min(open_price, close) * random.uniform(0.985, 1.00)

            data.append({
                'date': date,
                'open': round(open_price, 2),
                'high': round(high, 2),
                'low': round(low, 2),
                'close': round(close, 2),
                'volume': volume
            })

        df = pd.DataFrame(data)
        df.set_index('date', inplace=True)
        
        # 验证生成的数据是否符合条件
        if force_match and len(df) >= 20:
            df_indicators = calc_indicators(df)
            last_idx = len(df) - 1
            is_valid, issues = validate_stock_data(df, df_indicators, last_idx)
            if not is_valid:
                print(f"  警告: {stock_code} 生成数据验证失败: {issues}")
        
        return df

    except Exception as e:
        print(f"生成股票 {stock_code} 数据失败: {e}", file=sys.stderr)
        return None


def calc_indicators(df_daily):
    """计算日线指标：均线、成交量均线、斜率等"""
    df = df_daily.copy()
    df['ma5'] = df['close'].rolling(5).mean()
    df['ma10'] = df['close'].rolling(10).mean()
    df['ma20_vol'] = df['volume'].rolling(20).mean()
    # 10日线方向：今日ma10 > 昨日ma10
    df['ma10_up'] = df['ma10'] > df['ma10'].shift(1)
    # 历史放量大阳线候选标志
    df['big_yang'] = (df['close'] / df['close'].shift(1) >= 1.06) | (df['close'] / df['close'].shift(1) >= 1.095)
    df['vol_surge'] = df['volume'] > df['ma20_vol'] * 1.5
    df['history_signal'] = (df['big_yang'] & df['vol_surge']).rolling(20).max().fillna(0)
    return df


def validate_stock_data(df, df_indicators, idx):
    """
    验证股票数据是否满足所有条件
    返回: (是否满足, 问题列表)
    """
    issues = []
    
    if idx < 10:
        return False, ["数据不足10天"]
    
    today = df.iloc[idx]
    yesterday = df.iloc[idx-1]
    df_today = df_indicators.iloc[idx]
    
    # 条件1：当日收阴线
    if today['close'] >= today['open']:
        issues.append("非阴线")
    
    # 条件2：过去20天内有放量大阳线/涨停
    if df_today['history_signal'] == 0:
        issues.append("无放量大阳线")
    
    # 条件3：缩量阴线
    if today['volume'] >= yesterday['volume']:
        issues.append("非缩量")
    
    # 条件4：收盘价在10日线附近 ±2%
    price = today['close']
    ma10 = df_today['ma10']
    if not (0.98 * ma10 <= price <= 1.02 * ma10):
        issues.append(f"不在10日线附近(价格:{price:.2f}, MA10:{ma10:.2f})")
    
    # 条件5：MA10向上
    if not df_today['ma10_up']:
        issues.append("MA10向下")
    
    return len(issues) == 0, issues


